    private void FOTO_BTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FOTO_BTNMouseClicked
        JFileChooser arquivo = new JFileChooser();
    arquivo.setDialogTitle("Selecione uma imagem");
    arquivo.setFileSelectionMode(JFileChooser.FILES_ONLY);

    int retorno = arquivo.showOpenDialog(this);
    if (retorno == JFileChooser.APPROVE_OPTION) {
        File file = arquivo.getSelectedFile();
        caminhoImagem = file.getAbsolutePath(); // ✅ guarda o caminho

        try {
            ImageIcon icon = new ImageIcon(caminhoImagem);
            Image imgEscalada = icon.getImage().getScaledInstance(
                    foto_img.getWidth(),
                    foto_img.getHeight(),
                    Image.SCALE_SMOOTH
            );
            foto_img.setIcon(new ImageIcon(imgEscalada));
            foto_img.repaint();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar imagem: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Nenhuma imagem selecionada.");
    }
    }//GEN-LAST:event_FOTO_BTNMouseClicked

