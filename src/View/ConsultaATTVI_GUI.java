    private void ALTERARVI_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ALTERARVI_BTNActionPerformed

    String documento = doc_txt.getText().trim();
    if (documento.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Informe o documento do visitante!");
        return;
    }

    Visitante v = new Visitante();
    v.setNome(nome_txt.getText().trim());
    v.setDocumento(documento);
    v.setEmpresa(empresa_txt.getText().trim());
    v.setMotivo(motivo_txt.getText().trim());
    v.setContato(contato_txt.getText().trim());
    v.setColaborador(colaborador_txt.getText().trim());
    v.setStatus(statusvi_txt.getText().trim());
    v.setFoto(caminhoImagem); // ✅ salva o caminho atual ou novo

    boolean atualizado = Controller.Consultar_DAO.atualizarVisitante(v);
    if (atualizado) {
        JOptionPane.showMessageDialog(this, "Visitante atualizado com sucesso!");
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao atualizar visitante!");
    }

    }//GEN-LAST:event_ALTERARVI_BTNActionPerformed

