    private void CONSULTARVI_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CONSULTARVI_BTNActionPerformed

    String doc = doc_txt.getText().trim();
    if (doc.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Informe o documento!");
        return;
    }

    Visitante v = Consultar_DAO.consultarVisitante(doc);
    if (v != null) {
        nome_txt.setText(v.getNome());
        empresa_txt.setText(v.getEmpresa());
        motivo_txt.setText(v.getMotivo());
        contato_txt.setText(v.getContato());
        colaborador_txt.setText(v.getColaborador());
        codvi_txt.setText(String.valueOf(v.getCodigo()));
        dataentradavi_txt.setText(v.getDataEntrada());
        horaentradavi_txt.setText(v.getHoraEntrada());
        statusvi_txt.setText(v.getStatus());
        caminhoImagem = v.getFoto();

        if (v.getFoto() != null && !v.getFoto().isEmpty()) {
            ImageIcon icon = new ImageIcon(v.getFoto());
            Image img = icon.getImage().getScaledInstance(
                    foto_img.getWidth(),
                    foto_img.getHeight(),
                    Image.SCALE_SMOOTH
            );
            foto_img.setIcon(new ImageIcon(img));
        } else {
            foto_img.setIcon(null);
        }

    } else {
        JOptionPane.showMessageDialog(this, "Visitante não encontrado!");
        foto_img.setIcon(null);
        caminhoImagem = "";
    }

    }//GEN-LAST:event_CONSULTARVI_BTNActionPerformed

