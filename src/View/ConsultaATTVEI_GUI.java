    private void CONSULTARVEI_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CONSULTARVEI_BTNActionPerformed
        String placa = placa_txt.getText().trim();
    if (placa.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Informe a placa!");
        return;
    }

    Veiculo v = Consultar_DAO.consultarVeiculo(placa);
    if (v != null) {
        nome_txt.setText(v.getNome());
        codvei_txt.setText(String.valueOf(v.getCodigo()));
        placa_txt.setText(v.getPlaca());
        modelo_txt.setText(v.getModelo());
        marca_txt.setText(v.getMarca());
        cor_txt.setText(v.getCor());
        tipo_txt.setText(v.getTipo());
        categoria_txt.setText(v.getCategoria());
        statusvei_txt.setText(v.getStatus());
        dataentradavei_txt.setText(v.getDataEntrada());
        horaentradavei_txt.setText(v.getHoraEntrada());
    } else {
        JOptionPane.showMessageDialog(this, "Veículo não encontrado!");
    }
    }//GEN-LAST:event_CONSULTARVEI_BTNActionPerformed

