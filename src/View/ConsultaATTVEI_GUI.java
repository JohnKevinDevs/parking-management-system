    private void ALTERARVEI_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ALTERARVEI_BTNActionPerformed
        Veiculo v = new Veiculo();
    v.setNome(nome_txt.getText().trim());
    v.setPlaca(placa_txt.getText().trim());
    v.setModelo(modelo_txt.getText().trim());
    v.setMarca(marca_txt.getText().trim());
    v.setCor(cor_txt.getText().trim());
    v.setTipo(tipo_txt.getText().trim());
    v.setCategoria(categoria_txt.getText().trim());
    v.setStatus(statusvei_txt.getText().trim());

    boolean atualizado = Consultar_DAO.atualizarVeiculo(v);
    if (atualizado) {
        JOptionPane.showMessageDialog(this, "Veículo atualizado com sucesso!");
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao atualizar veículo!");
    }
    }//GEN-LAST:event_ALTERARVEI_BTNActionPerformed

