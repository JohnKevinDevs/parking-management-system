    private void BUSCARINFO_BTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BUSCARINFO_BTNMouseClicked
        String placa = placa_txt.getText().trim();

    if (placa.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Digite a placa do veículo!");
        return;
    }

    String[] veiculo = Controller.Saida_DAO.buscarVeiculo(placa);
    if (veiculo != null) {
        modelo_txt.setText(veiculo[0]);
        cor_txt.setText(veiculo[1]);
        colaborador_txt.setText(veiculo[2]);
    } else {
        JOptionPane.showMessageDialog(this, "Veículo não encontrado!");
    }
    }//GEN-LAST:event_BUSCARINFO_BTNMouseClicked

