    private void SAIDA_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SAIDA_BTNActionPerformed
        String placa = placa_txt.getText().trim();
    String modelo = modelo_txt.getText().trim();
    String cor = cor_txt.getText().trim();
    String colaborador = colaborador_txt.getText().trim();

    if (placa.isEmpty() || modelo.isEmpty() || colaborador.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Preencha os campos obrigatórios!");
        return;
    }

    boolean sucesso = Controller.Saida_DAO.registrarSaida(placa, modelo, cor, colaborador);

    if (sucesso) {
        JOptionPane.showMessageDialog(this, "Saída registrada com sucesso!");
        placa_txt.setText("");
        modelo_txt.setText("");
        cor_txt.setText("");
        colaborador_txt.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao registrar saída!");
    }
    }//GEN-LAST:event_SAIDA_BTNActionPerformed

