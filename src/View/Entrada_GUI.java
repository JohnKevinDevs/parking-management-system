    private void BUSCARINFO_BTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BUSCARINFO_BTNMouseClicked
        String documento = doc_txt.getText().trim();
    String placa = placa_txt.getText().trim();

    if (!documento.isEmpty()) {
        String[] visitante = Controller.Entrada_DAO.buscarVisitante(documento);
        if (visitante != null) {
            nome_txt.setText(visitante[0]);
            motivo_txt.setText(visitante[1]);
        } else {
            JOptionPane.showMessageDialog(this, "Visitante não encontrado!");
        }
    }

    if (!placa.isEmpty()) {
        String[] veiculo = Controller.Entrada_DAO.buscarVeiculo(placa);
        if (veiculo != null) {
            modelo_txt.setText(veiculo[0]);
            categoria_txt.setText(veiculo[1]);
        } else {
            JOptionPane.showMessageDialog(this, "Veículo não encontrado!");
        }
    }
    }//GEN-LAST:event_BUSCARINFO_BTNMouseClicked

