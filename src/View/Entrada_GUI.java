    private void ENTRADA_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ENTRADA_BTNActionPerformed
        String documento = doc_txt.getText().trim();
    String nome = nome_txt.getText().trim();
    String motivo = motivo_txt.getText().trim();
    String placa = placa_txt.getText().trim();
    String modelo = modelo_txt.getText().trim();
    String categoria = categoria_txt.getText().trim();
    String colaborador = colaborador_txt.getText().trim();
    String vaga = vaga_txt.getText().trim();
    String plano = plano_txt.getText().trim();

    if (documento.isEmpty() || placa.isEmpty() || colaborador.isEmpty() || vaga.isEmpty() || plano.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios!");
        return;
    }

    boolean sucesso = Controller.Entrada_DAO.registrarEntrada(
        documento, nome, motivo, placa, modelo, categoria, colaborador, vaga, plano
    );

    if (sucesso) {
        JOptionPane.showMessageDialog(this, "Entrada registrada com sucesso!");
        doc_txt.setText("");
        nome_txt.setText("");
        motivo_txt.setText("");
        placa_txt.setText("");
        modelo_txt.setText("");
        categoria_txt.setText("");
        colaborador_txt.setText("");
        vaga_txt.setText("");
        plano_txt.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao registrar entrada!");
    }
    }//GEN-LAST:event_ENTRADA_BTNActionPerformed

