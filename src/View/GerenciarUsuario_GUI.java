    private void ALTERARVI_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ALTERARVI_BTNActionPerformed
        Usuario_Controller controller = new Usuario_Controller();
        if (controller.atualizarUsuario(nome_txt.getText(), usuario_txt.getText(), senha_txt.getText(), tipo_txt.getText())) {
        JOptionPane.showMessageDialog(this, "Usuário atualizado com sucesso!");
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao atualizar usuário!");
    }
    }//GEN-LAST:event_ALTERARVI_BTNActionPerformed

