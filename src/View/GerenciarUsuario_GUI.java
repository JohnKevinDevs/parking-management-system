    private void EXCLUIR_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EXCLUIR_BTNActionPerformed
        Usuario_Controller controller = new Usuario_Controller();
        int confirm = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este usuário?");
    if (confirm == JOptionPane.YES_OPTION) {
        if (controller.excluirUsuario(usuario_txt.getText())) {
            JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário!");
        }
    }
    }//GEN-LAST:event_EXCLUIR_BTNActionPerformed

