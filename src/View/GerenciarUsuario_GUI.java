    private void CONSULTARVI_BTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CONSULTARVI_BTNActionPerformed
        Usuario_Controller controller = new Usuario_Controller();
        Usuario u = controller.consultarUsuario(usuario_txt.getText());

        if (u != null) {
            nome_txt.setText(u.getNome());
            senha_txt.setText(u.getSenha());
            tipo_txt.setText(u.getTipo());
        } else {
            JOptionPane.showMessageDialog(this, "Usuário não encontrado!");
        }
    }//GEN-LAST:event_CONSULTARVI_BTNActionPerformed

